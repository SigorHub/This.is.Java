package Chap03;

public class BitLogicExample {				//비트 논리 연산자
	public static void main(String[] args) {
		System.out.println(45&25);			//9 출력
		System.out.println(45|24);			//61 출력
		System.out.println(45^25);			//52 출력
		System.out.println(~45);			//-46 출력
		System.out.println(~25);			//-25 출력
	}
}