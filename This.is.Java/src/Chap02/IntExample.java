package Chap02;

public class IntExample {
	public static void main(String[] args) {
		int var1 = 10;					//10을 10진수로 저장
		int var2 = 012;					//10을 8진수로 저장
		int var3 = 0xA;					//10을 16진수로 저장

		System.out.println(var1);		//10출력
		System.out.println(var2);		//10출력
		System.out.println(var3);		//10출력
	}
}
